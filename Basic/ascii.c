#include <stdio.h>
void main() {    
    char ch = 'a';
    printf("%d ", ch);    

    ch = 'B'; // 66
    printf("%d ", ch);  
    ch = 'C'; // 67
    printf("%d ", ch);  
    ch = 'D'; // 68
    printf("%d ", ch);  
    ch = 'E'; // 69
    printf("%d ", ch);  

    int num = 65;
    printf("%c ", num);
    num = 98;
    printf("%c ", num);
}